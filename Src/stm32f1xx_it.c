/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
    uint32_t errorflags = 0U;

	errorflags = (USART1->SR & (uint32_t)(USART_SR_PE | USART_SR_FE | USART_SR_ORE | USART_SR_NE));

	if(errorflags == 0U)
	{
		if((USART1->SR & USART_SR_RXNE) != RESET)
		{
			uint8_t rbyte = (uint8_t)(USART1->DR & (uint8_t)0x00FF);
			uint16_t i = (uint16_t)(rx_buffer_head + 1) % UART_RX_BUFFER_SIZE;

			if(i != rx_buffer_tail)
			{
				rx_buffer[rx_buffer_head] = rbyte;
				rx_buffer_head = i;
			}
		}
		else if(((USART1->SR & USART_SR_TC) != RESET) && ((USART1->CR1 & USART_CR1_TCIE) != RESET))
		{
			__HAL_UART_DISABLE_IT(&huart1, UART_IT_TC);
			huart1.gState = HAL_UART_STATE_READY;
		}

	}
	else
	{
		uint8_t rbyte = (uint8_t)(USART1->DR & (uint8_t)0x00FF);
		UNUSED(rbyte);
		HAL_UART_Abort_IT(&huart1);
		__HAL_UART_ENABLE_IT(&huart1, UART_IT_RXNE);
	}

	return;
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

